'''
Operasi aritmatika dalam python terdapat urutan operasi
1. () Tanda Kurung
2. ** Pemangkatan
3. * / % Perkalian, Pembagian, Modulus
4. + - Tambah, Kurang
'''

a = 10
b = 2

# Operasi Penjumlahan (+)
hasil = a + b
print("Penjumlahan (+)")
print(a,'+',b,'=',hasil,'\n')

# Operasi Pengurangan (-)
hasil = a - b
print("Pengurangan (-)")
print(a,'-',b,'=',hasil,'\n')

# Operasi Perkalian (*)
hasil = a * b
print("Perkalian (*)")
print(a,'*',b,'=',hasil,'\n')

# Operasi Pembagian (/)
hasil = a / b
print("Pembagian (/)")
print(a,'/',b,'=',hasil,'\n')

# Operasi Modulus (%)
hasil = a % b
print("Modulus (%)")
print(a,'%',b,'=',hasil,'\n')

# Operasi Floor Division (//)
hasil = a // b
print("Floor Division (//)")
print(a,'//',b,'=',hasil,'\n')

# Operasi Pangkat (**)
hasil = a ** b
print("Pangkat (**)")
print(a,'**',b,'=',hasil,'\n')